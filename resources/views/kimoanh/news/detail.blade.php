      <h1 class="news-name">“Chìa khóa” thành công của Kim Oanh Real</h1>
      <div class="news-info">
        <time>02/06/2018</time>
      </div>
      <div class="content"><p style="text-align: justify;">Xây dựng một tập thể gắn kết dựa trên nền tảng văn hóa doanh nghiệp là yếu tố then chốt giúp Công ty cổ phần Địa ốc Kim Oanh (Kim Oanh Real) gặt hái thành công trong suốt 10 năm qua.&nbsp;</p>
<p style="text-align: justify;"><strong>Công ty là ngôi nhà thứ hai</strong></p>
<p style="text-align: justify;">Trong đêm gala “Kim Oanh – 10 năm tiếp bước” nhân kỷ niệm 10 năm thành lập vào cuối tháng 5 vừa qua, chính quyền thị xã Bến Cát và Ban lãnh đạo Kim Oanh Real đã trao tặng bằng khen và phần thưởng xứng đáng cho các nhân sự đã gắn bó lâu năm và đóng góp tốt cho sự phát triển của công ty. Ở Kim Oanh Real có rất nhiều nhân sự đã gắn bó từ những ngày đầu thành lập và cùng trải qua những năm tháng thăng trầm để đưa công ty lên vị trí hàng đầu trong lĩnh vực phân phối bất động sản. Đó có thể là một lãnh đạo, một quản lý cấp trung và cũng có thể là một thư ký hay nhân viên bảo vệ bình thường. Điểm chung của họ là đều xem Kim Oanh Real như ngôi nhà thứ hai, nơi làm việc, trau dồi chuyên môn và chia sẻ mọi thứ trong cuộc sống.&nbsp;</p>
<p style="text-align: center;"><img alt="" src="{{ config('setting.folder_image_public') }}Data/Sites/1/media/tin_tuc/2018/06/chia-khoa-thanh-cong.jpg" /></p>
<p style="text-align: center;"><em>Tại Kim Oanh Real, tất cả nhân viên đều xem nhau như người một nhà, cùng nhau chia sẻ, cùng nhau phát triển</em></p>
<p style="text-align: left;"><span style="text-align: justify;">Khi triển khai công việc, mỗi cá nhân đều sẵn sàng “xung trận” với tinh thần cao nhất. Khi gặp khó khăn, tất cả đều tập trung giải quyết vấn đề, làm sao để công việc “về đích” đúng tiến độ đề ra với hiệu quả cao nhất. “Các lãnh đạo Kim Oanh Real xem nhân viên như những cộng sự thân thiết, sẵn sàng tranh luận để tìm ra phương án tốt nhất chứ không chỉ đạo một chiều. Chính điều này giúp chúng tôi chủ động suy nghĩ và thấy mình cùng đồng cam cộng khổ, cùng đóng một vai trò quan trọng trong thành công chung chứ không chỉ là người thừa hành”, Lê Thị Thanh Phương, một thư ký vừa được bổ nhiệm chức Trợ lý Tổng giám đốc, chia sẻ.&nbsp;</span><em><br />
</em></p>
<p style="text-align: justify;">Thực tế, ngay cả trong các chương trình thiện nguyện trên khắp mọi miền đất nước, Kim Oanh Real cũng luôn tập hợp được rất đông lãnh đạo, nhân viên cùng tham gia. Chính họ là những người đã trích lương, thưởng để đóng góp cho Quỹ Từ thiện Kim Oanh và tự tay làm mọi việc để tiết giảm chi phí trong khâu tổ chức. “Nhiều lúc tôi thấy hơn 1.000 nhân viên của mình như những chiến binh trên mặt trận kinh doanh cũng như công tác xã hội”, bà Đặng Thị Kim Oanh, Tổng giám đốc Kim Oanh Real, nhận xét.&nbsp;</p>
<p style="text-align: center;"><img alt="" src="{{ config('setting.folder_image_public') }}Data/Sites/1/media/tin_tuc/2018/06/chia-khoa-thanh-cong-1.jpg" /></p>
<p style="text-align: center;"><em>Tất cả đều tích cực tham gia, đóng góp cùng mọi hành trình thiện nguyện của công ty.</em></p>
<p style="text-align: center;">
</p>
<div style="text-align: justify;"><strong>Mỗi nhân viên là một đại sứ!</strong><br />
</div>
<p>&nbsp;</p>
<p style="text-align: justify;">Bà Oanh tiết lộ, mỗi năm Kim Oanh Real dành ngân sách khá lớn để tưởng thưởng cho những nhân viên có đóng góp thiết thực. Rất nhiều lãnh đạo, nhân viên xuất sắc đã được công ty hỗ trợ mua nhà, ô tô, cưới hỏi… Bên cạnh đó, Kim Oanh Real còn tổ chức các chương trình du lịch trong và ngoài nước dành cho nhân viên và gia đình kết hợp teambuilding.&nbsp;</p>
<p style="text-align: justify;">“Tại Kim Oanh Group nói chung, Kim Oanh Real nói riêng, chúng tôi luôn tìm cách giúp nhân viên phát huy hết năng lực của mình. Mọi đóng góp của nhân viên đều được ghi nhận kịp thời và tưởng thưởng xứng đáng. Có những thời điểm công ty còn quyết định cắt lợi nhuận để có ngân sách chăm lo đời sống cho nhân viên.”, bà Oanh nói và cho biết theo quan điểm của bà, mỗi nhân viên chính là một đại sứ thương hiệu. Chỉ khi họ cảm thấy tự hào, thỏa mãn tại nơi làm việc thì họ mới gắn bó lâu dài và hết mình vì công việc chung. Khi đó, họ chính là những đại sứ đưa hình ảnh công ty đi vào lòng khách hàng, đối tác…&nbsp;</p>
<p>
</p>
<div style="text-align: justify;">
<div style="text-align: center;"><img alt="" src="{{ config('setting.folder_image_public') }}Data/Sites/1/media/tin_tuc/2018/06/chia-khoa.jpg" /><br />
</div>
</div>
<p style="text-align: center;"><em>Mọi đóng góp của nhân viên đều được ghi nhận kịp thời và tưởng thưởng xứng đáng. Ảnh: ông Nguyễn Thuận, chủ tịch HĐQT và bà Đặng Thị Kim Oanh, Tổng giám đốc Kim Oanh Real trao thưởng cho các nhân viên đã gắn bó với công ty từ 8-10 năm tại đêm gala "Kim Oanh - 10 năm tiếp bước".&nbsp;</em></p>
<p style="text-align: justify;">Một sự khác biệt lớn là ở những thời điểm thị trường khó khăn, nhiều doanh nghiệp thường dung biện pháp cắt giảm nhân sự để tiết giảm chi phí thì ở Kim Oanh Real lại hoàn toàn ngược lại. Nhiều năm qua, mỗi năm công ty đều tuyển thêm hàng trăm nhân viên ở tất cả các bộ phận. Cùng với đó, những chương trình đào tạo nghiệp vụ, tham quan học hỏi thường xuyên được tổ chức để giúp nhân viên nâng cao năng lực, tích lũy thêm kiến thức phục vụ cho công việc.</p>
<p style="text-align: justify;">Hiện nay, hệ thống Kim Oanh Real gồm sáu chi nhánh hoạt động tại Bình Dương, TPHCM và Đồng Nai phục vụ lượng khách hàng đông đảo đến từ khắp nơi trên cả nước. Bà Oanh cho biết, với việc Kim Oanh Group đang nắm giữ quỹ đất sạch lên đến 550 ha và đang tiếp tục tích lũy thêm, sắp tới công ty sẽ không dừng lại mà vẫn tăng cường công tác tuyển dụng và mở rộng hệ thống. Hơn nữa, giai đoạn năm năm tới, công ty còn triển khai chiến lược “lấn sân” sang mảng cung cấp dịch vụ cho các khu đô thị và phân khúc bất động sản nghỉ dưỡng. “Đó sẽ là thời điểm tập thể lãnh đạo, nhân viên Kim Oanh Real sẽ cùng chung vai sát cánh đưa thương hiệu công ty lên một tầm cao mới””, bà Oanh nhấn mạnh.</p>
